package acmicpc;

import java.io.*;

public class 사다리조작_15684 {
    public static void main(String[] args){
        BufferedReader br = new BufferedReader(new InputStreamReader(System.in));
        BufferedWriter bw = new BufferedWriter(new OutputStreamWriter(System.out));
        try {
            int N = br.read();
            bw.write((int)N+"\n");
            bw.flush();
        } catch (IOException e) {
            e.printStackTrace();
        }

    }
}
